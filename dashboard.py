@callback(
    Output("kpi-row", "children"),
    Input("filter-neighbourhood", "value"),
    Input("filter-room-type",     "value"),
)
def update_kpis(neighbourhoods, room_types):
    df = filter_listings(neighbourhoods, room_types)

    def kpi_card(label, value, sub, highlight=False):
        return html.Div([
            html.P(label,
                   style={"margin": "0 0 4px 0", "fontSize": "11px",
                          "fontWeight": "600", "color": COLORS["subtext"],
                          "textTransform": "uppercase", "letterSpacing": "0.05em"}),
            html.P(value,
                   style={"margin": "0 0 2px 0", "fontSize": "28px",
                          "fontWeight": "700",
                          "color": COLORS["alert"] if highlight else COLORS["primary"]}),
            html.P(sub,
                   style={"margin": 0, "fontSize": "11px", "color": COLORS["subtext"]}),
        ], style={**CARD_STYLE, "textAlign": "center"})

    return [
        kpi_card("Total Listings",
                 f"{len(df):,}",
                 "active listings (prices ≤ $2K)", highlight=True),
        kpi_card("Median Nightly Price",
                 f"${df['price'].median():,.0f}",
                 "per night (USD)"),
        kpi_card("Avg Availability",
                 f"{df['availability_365'].mean():.0f} days/yr",
                 "out of 365 — higher = less booked"),
        kpi_card("Total Reviews",
                 f"{df['number_of_reviews'].sum():,}",
                 "cumulative proxy for bookings"),
    ]


@callback(
    Output("chart-neighbourhood", "figure"),
    Input("filter-neighbourhood", "value"),
    Input("filter-room-type",     "value"),
)
def update_neighbourhood(neighbourhoods, room_types):
    df = filter_listings(neighbourhoods, room_types)
    nb = (df.groupby("neighbourhood")
            .agg(count=("id", "count"), median_price=("price", "median"))
            .reset_index()
            .sort_values("count", ascending=True)
            .tail(20))

    fig = go.Figure()
    fig.add_trace(go.Bar(
        x=nb["count"], y=nb["neighbourhood"],
        orientation="h",
        marker=dict(
            color=nb["median_price"],
            colorscale=[[0, COLORS["muted"]],
                        [0.5, COLORS["secondary"]],
                        [1, COLORS["primary"]]],
            showscale=True,
            colorbar=dict(title="Median<br>Price ($)",
                          tickfont=dict(size=10), len=0.8),
        ),
        hovertemplate=(
            "<b>%{y}</b><br>"
            "Listings: %{x}<br>"
            "Median price: $%{marker.color:.0f}"
            "<extra></extra>"
        ),
    ))
    fig.update_layout(**base_layout(
        title="Listings per Neighbourhood  (colour = median nightly price)",
        xaxis_title="Number of Listings",
    ))
    return fig


@callback(
    Output("chart-price-dist", "figure"),
    Input("filter-neighbourhood", "value"),
    Input("filter-room-type",     "value"),
)
def update_price_dist(neighbourhoods, room_types):
    df = filter_listings(neighbourhoods, room_types)
    df = df[df["price"] <= 800]

    fig = go.Figure()
    for rt in ["Entire home/apt", "Private room", "Hotel room", "Shared room"]:
        subset = df[df["room_type"] == rt]["price"]
        if len(subset) == 0:
            continue
        fig.add_trace(go.Box(
            y=subset, name=rt,
            marker_color=ROOM_COLORS.get(rt, COLORS["muted"]),
            boxmean=True,
            hovertemplate=f"<b>{rt}</b><br>$%{{y:.0f}}<extra></extra>",
        ))
    fig.update_layout(**base_layout(
        title="Price Distribution by Room Type",
        yaxis_title="Nightly Price ($)",
        showlegend=False,
    ))
    fig.update_xaxes(tickangle=-20)
    return fig


@callback(
    Output("chart-reviews-trend", "figure"),
    Input("filter-neighbourhood", "value"),
    Input("filter-room-type",     "value"),
)
def update_reviews(neighbourhoods, room_types):
    df_l = filter_listings(neighbourhoods, room_types)

    # If no filter → use pre-aggregated monthly totals (fast)
    if not neighbourhoods and not room_types:
        monthly = reviews_monthly.copy()
    else:
        listing_ids = set(df_l["id"].tolist())
        monthly = (
            reviews_per_lst[reviews_per_lst["listing_id"].isin(listing_ids)]
            .groupby("year_month")["reviews"]
            .sum()
            .reset_index()
            .sort_values("year_month")
        )

    monthly = monthly[monthly["year_month"] >= "2020-01-01"]

    fig = go.Figure()
    fig.add_trace(go.Scatter(
        x=monthly["year_month"], y=monthly["reviews"],
        mode="lines",
        line=dict(color=COLORS["primary"], width=2.5),
        fill="tozeroy",
        fillcolor="rgba(38,70,83,0.08)",
        hovertemplate="%{x|%b %Y}: <b>%{y:,}</b> reviews<extra></extra>",
    ))

    if len(monthly):
        peak = monthly.loc[monthly["reviews"].idxmax()]
        fig.add_annotation(
            x=peak["year_month"], y=peak["reviews"],
            text=f"<b>Peak: {peak['reviews']:,}</b>",
            showarrow=True, arrowhead=2,
            arrowcolor=COLORS["alert"],
            font=dict(color=COLORS["alert"], size=11),
            ax=0, ay=-30,
        )

    fig.update_layout(**base_layout(
        title="Monthly Review Activity  (2020 – Present)",
        yaxis_title="Reviews / Month",
    ))
    return fig


@callback(
    Output("chart-availability", "figure"),
    Input("filter-neighbourhood", "value"),
    Input("filter-room-type",     "value"),
)
def update_availability(neighbourhoods, room_types):
    # Calendar is not filtered by room type (no room_type col in calendar).
    # If neighbourhood filter applied, use listings availability_365 as proxy.
    df_l = filter_listings(neighbourhoods, room_types)

    if not neighbourhoods and not room_types:
        avail = cal_monthly.copy()
    else:
        # Use avg availability_365 per month as a lightweight proxy
        avg_avail_pct = df_l["availability_365"].mean() / 365 * 100
        avail = cal_monthly.copy()
        # Scale the seasonal curve to the filtered subset's overall availability
        scale = avg_avail_pct / cal_monthly["pct_available"].mean()
        avail["pct_available"] = (avail["pct_available"] * scale).clip(0, 100)

    avg = avail["pct_available"].mean()

    fig = go.Figure()
    fig.add_trace(go.Bar(
        x=avail["month_name"], y=avail["pct_available"],
        marker_color=[
            COLORS["alert"] if v < avg else COLORS["secondary"]
            for v in avail["pct_available"]
        ],
        hovertemplate="%{x}: <b>%{y:.1f}%</b> available<extra></extra>",
    ))
    fig.add_hline(y=avg, line_dash="dot", line_color=COLORS["subtext"],
                  annotation_text=f"Avg {avg:.1f}%",
                  annotation_font_color=COLORS["subtext"],
                  annotation_font_size=11)
    fig.update_layout(**base_layout(
        title="Avg Nightly Availability by Month",
        yaxis_title="% Available Nights",
    ))
    fig.update_yaxes(range=[0, 100], ticksuffix="%")
    return fig


@callback(
    Output("chart-scatter", "figure"),
    Input("filter-neighbourhood", "value"),
    Input("filter-room-type",     "value"),
)
def update_scatter(neighbourhoods, room_types):
    df = filter_listings(neighbourhoods, room_types)
    df = df[df["price"] <= 800]
    df = df.sample(min(len(df), 1500), random_state=42)

    fig = go.Figure()
    for rt in ALL_ROOM_TYPES:
        sub = df[df["room_type"] == rt]
        if len(sub) == 0:
            continue
        fig.add_trace(go.Scatter(
            x=sub["price"], y=sub["number_of_reviews"],
            mode="markers", name=rt,
            marker=dict(
                color=ROOM_COLORS.get(rt, COLORS["muted"]),
                size=6, opacity=0.65,
                line=dict(width=0.5, color="white"),
            ),
            hovertemplate=(
                "<b>%{text}</b><br>"
                "Price: $%{x}<br>"
                "Reviews: %{y}<br>"
                f"Type: {rt}<extra></extra>"
            ),
            text=sub["name"].str[:40],
        ))

    fig.add_vrect(
        x0=80, x1=300,
        fillcolor=COLORS["accent"], opacity=0.08,
        annotation_text="Sweet spot ($80–$300)",
        annotation_position="top left",
        annotation_font_color=COLORS["accent"],
        annotation_font_size=11,
        line_width=0,
    )
    fig.update_layout(**base_layout(
        title="Nightly Price vs. Total Reviews  (sampled, ≤ $800)",
        xaxis_title="Nightly Price ($)",
        yaxis_title="Number of Reviews",
        showlegend=True,
    ))
    fig.update_layout(legend=dict(
        orientation="h", yanchor="bottom", y=1.02,
        xanchor="right", x=1, font=dict(size=11),
    ))
    return fig


