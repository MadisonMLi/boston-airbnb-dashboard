def filter_listings(neighbourhoods, room_types):
    df = listings.copy()
    if neighbourhoods:
        df = df[df["neighbourhood"].isin(neighbourhoods)]
    if room_types:
        df = df[df["room_type"].isin(room_types)]
    return df

